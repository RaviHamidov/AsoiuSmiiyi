                  <div class="blog-desc-big mt-2">
                    <p></p>
                    İyunun 28-də Azərbaycan Dövlət Neft və Sənaye
                    Universitetinin (ADNSU) nəzdində fəaliyyət göstərən
                    Azərbaycan-Fransız Universitetində (UFAZ) Məzun Günü
                    keçirilib. Tədbirdə Azərbaycan Respublikasının Təhsil
                    Nazirinin müavini Firudin Qurbanov, ADNSU-nun rektoru
                    professor Mustafa Babanlı, Fransanın Azərbaycandakı səfiri
                    Zakari Qross, UFAZ-ın İcraçı Direktoru Vazeh Əskərov,
                    universitetin professor-müəllim heyəti və məzunlar iştirak
                    ediblər. Əvvəlcə Respublikasının Azərbaycan Dövlət Himni
                    səsləndirilib. ADNSU rektoru, professor Mustafa Babanlı
                    çıxışında UFAZ-ın bu il 39 magistr və 139 bakalavr məzunun
                    olduğunu bildirib. “7 UFAZ məzunu bu il dünyanın TOP 50
                    siyahısında olan universitetlərə tam təqaüdlə qəbul olub”.
                    Rektor M.Babanlı UFAZ-ın magistr məzunlarını bu layihədə
                    və ADNSU-da müəllim və tədqiqat sahəsində çalışmağa dəvət
                    edib. Rektor valideynləri təbrik edib və məzunlara uğurlar
                    arzulayıb. Tədbirdə çıxış edən Təhsil nazirinin müavini
                    Firudin Qurbanov UFAZ tələbələrinin məzun olduğu
                    ixtisasların ölkə üçün vacibliyini qeyd edib və məzunları
                    ölkəmizin müasir quruculuğunda və azad olunmuş ərazilərin
                    yenidənqurulmasında aktiv iştirak etməyə səsləyib.
                    Fransanın ölkəmizdəki səfiri Z.Qross UFAZ-ı seçdiklərinə
                    görə gənclərə təşəkkür edib “Biz Azərbaycanın gələcəyinə
                    nikbin baxırıq. Fransa tərəfdaş olaraq çiçəklənən, inkişaf
                    edən, suveren və müstəqil Azərbaycan görmək istəyir. UFAZ
                    layihəsi də bunun bir parçasıdır”. Səfir qeyd edib ki,
                    inkişaf edən Azərbaycan qarşısında duran çağırışlardan
                    biri də enerji tranzisiyası – karbon emissiyasının
                    azaldılmasıdır. O UFAZ məzunlarını öz bilikləri ilə bu işə
                    töhfə verməyə çağırıb.

                    <p>
                      UFAZ-ın İcraçı direktoru Vazeh Əskərov və Baş katibi
                      Elizaveta Bydanova tələbələrin nailiyyətləri barədə
                      tədbir iştirakçılarına məlumat verib. Bildirilib ki, bu
                      il bakalavr pilləsini bitirən 29 nəfər UFAZ məzunu
                      xaricdə təhsillərini magistratura pilləsində davam
                      etdirəcək.
                    </p>

                    <p>
                      UFAZ məzunları Ləman Cəlilova və İlkin Səfərli isə
                      universitetdə təhsil aldıqları müddət ərzində aldıqları
                      təəssüratları bölüşüblər. Rəsmi hissədən sonra məzunlara
                      sertifikatlar təqdim olunub. Tədbir konsert proqramı ilə
                      davam edib.
                    </p>

